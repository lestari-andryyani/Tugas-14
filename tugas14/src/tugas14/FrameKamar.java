    private void populateTable() {
        model.setRowCount(0);
        try {
            ArrayList<kamar> daftar = koneksi.getKamar();
            for (kamar p : daftar) {
                Object[] isiData = {p.getNoKamar(), p.getStatusKamar(), p.getFiturKamar(), p.getJenisKamar(), p.getLantai()};
                model.addRow(isiData);
            }
        } catch (SQLException ex) {
            System.out.println("Eksepsi: " + ex.getMessage());
            JOptionPane.showMessageDialog(null, "Exception: " + ex.getMessage());
        }

    }
    
    private void populateTable(String keyword) {
        model.setRowCount(0);
        try {
            ArrayList<kamar> daftar = koneksi.cariKamar(keyword);
            for (kamar p : daftar) {
                Object[] isiData = {p.getNoKamar(), p.getStatusKamar(), p.getFiturKamar(), p.getJenisKamar(), p.getLantai()};
                model.addRow(isiData);
            }
        } catch (SQLException ex) {
            System.out.println("Eksepsi: " + ex.getMessage());
            JOptionPane.showMessageDialog(null, "Exception: " + ex.getMessage());
        }

    }
    
