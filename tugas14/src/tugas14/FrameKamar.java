    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        // TODO add your handling code here:

        int selectedBaris = tableKamar.getSelectedRow();
	int Baris = tableKamar.convertRowIndexToModel(selectedBaris);

	String noKamar = model.getValueAt(Baris, 0).toString();

	int jawaban = JOptionPane.showConfirmDialog(null, "Hapus Data? " + noKamar);
	if (jawaban == JOptionPane.YES_OPTION){
		model.removeRow(Baris);
	}
    }//GEN-LAST:event_btnHapusActionPerformed

    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        // TODO add your handling code here:
        String noKamar = textNoKamar.getText();
        String statusKamar = cbStatusKamar.getSelectedItem().toString();
        String fiturKamar = cbFiturKamar.getSelectedItem().toString();
        String jenisKamar = cbJenisKamar.getSelectedItem().toString();
        String lantai = cbLantai.getSelectedItem().toString();
        kamar p = new kamar(noKamar, statusKamar, fiturKamar, jenisKamar, Integer.parseInt(lantai));

        if (btnSubmit.getText().equals("Simpan")) {
            try {
                if (koneksi.insertKamar(p)) {
                    JOptionPane.showMessageDialog(this, "Berhasil Insert");
                    populateTable();
                } else {
                    JOptionPane.showMessageDialog(this, "Gagal Insert");
                }
            } catch (SQLException ex) {
                System.out.println("Eksepsi: " + ex.getMessage());
                JOptionPane.showMessageDialog(null, "Exception: " + ex.getMessage());
            }
        } 
        else {
            try {
                int jawaban = JOptionPane.showConfirmDialog(this, "Ubah Data " + p.getNoKamar() + "?", "Ubah", JOptionPane.YES_NO_OPTION);
                if (jawaban == JOptionPane.YES_OPTION) {
                    if (koneksi.updateKamar(p)) {
                        JOptionPane.showMessageDialog(this, "Berhasil Update");
                        populateTable();
                    } else {
                        JOptionPane.showMessageDialog(this, "Gagal Update");
                    }
                }
            } catch (SQLException ex) {
                System.out.println("Eksepsi: " + ex.getMessage());
                JOptionPane.showMessageDialog(null, "Exception: " + ex.getMessage());
            }
        }
        kosongkan();
    }//GEN-LAST:event_btnSubmitActionPerformed

